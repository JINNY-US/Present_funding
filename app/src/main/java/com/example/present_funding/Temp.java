package com.example.present_funding;

public class Temp {
    private String support_uid, support_name, temp, val;

    public Temp() {
    }

    public String getSupport_uid() {
        return support_uid;
    }

    public void setSupport_uid(String support_uid) {
        this.support_uid = support_uid;
    }

    public String getSupport_name() {
        return support_name;
    }

    public void setSupport_name(String support_name) {
        this.support_name = support_name;
    }

    public String getTemp() {
        return temp;
    }

    public void setTemp(String temp) {
        this.temp = temp;
    }

    public String getVal() { return val; }

    public void setVal(String val) { this.val = val; }
}
